#[derive(Clone)]
pub struct Package {
    pub id: String,
    pub name: String,
    pub version: String,
    pub permissions: String,
    pub exec_path: String,
    pub installed: bool,
    pub builtin: bool,
}

#[derive(Clone)]
pub struct PackageLaunch {
    pub pid: usize,
    pub name: String,
    pub exec_path: String,
}

struct ArchiveManifest {
    id: String,
    name: String,
    command: String,
    version: String,
    icon: String,
    category: String,
    permission: String,
    exec_path: String,
    aliases: String,
    associations: String,
    dependencies: Vec<String>,
    payloads: Vec<PackagePayload>,
    min_os_version: u64,
    trust_manifest: String,
    installed_manifest: String,
}

struct PackageSignature {
    key: String,
    algorithm: String,
    package_id: String,
    package_version: String,
    issued_epoch: u64,
    manifest_sha256: String,
    signature: String,
}

struct TrustedKey {
    id: String,
    algorithm: String,
    status: String,
    public_key: [u8; 32],
    not_before: u64,
    not_after: u64,
    generation: u64,
}

struct PackageVerification {
    id: String,
    command: String,
    version: String,
    key: String,
    algorithm: String,
    manifest_sha256: String,
    signature_path: String,
    dependencies: Vec<String>,
    payloads: Vec<PackagePayload>,
}

struct OwnerRecord {
    id: String,
    name: String,
    command: String,
    version: String,
    source: String,
    manifest_path: String,
    installed_sha256: String,
    package_manifest_sha256: String,
    verified_by: String,
    algorithm: String,
    dependencies: Vec<String>,
    payloads: Vec<PackagePayload>,
}

#[derive(Clone)]
struct PackagePayload {
    target: String,
    source: String,
    sha256: String,
    mode: u16,
}

struct RollbackFile {
    path: String,
    before: Option<Vec<u8>>,
    metadata: Option<crate::vfs::FileMetadata>,
}

